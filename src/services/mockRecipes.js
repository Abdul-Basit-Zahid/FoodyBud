const VARIANT_TAGS = [
  'Street Style',
  'Home Style',
  'Spiced',
  'Smoky',
  'Zesty',
  'Creamy',
  'Grilled',
  'Roasted',
  'Quick',
  'Herby',
  'Crispy',
  'Slow-Cooked'
];

export const getExpandedRecipes = (cuisine, count = 30) => {
  const base = MOCK_RECIPES[cuisine] || DEFAULT_RECIPES;
  const expanded = base.map((dish) => ({ ...dish }));

  let variantIndex = 0;
  while (expanded.length < count) {
    const source = base[variantIndex % base.length];
    const tag = VARIANT_TAGS[variantIndex % VARIANT_TAGS.length];
    const version = Math.floor(variantIndex / VARIANT_TAGS.length) + 1;
    const suffix = version > 1 ? `${tag} ${version}` : tag;
    expanded.push({
      ...source,
      name: `${source.name} (${suffix})`
    });
    variantIndex += 1;
  }

  return expanded.slice(0, count);
};

